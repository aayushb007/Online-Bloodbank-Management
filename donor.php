      <h1> Donating blood is a simple thing to do, but it can make a big difference in the lives of others. The donation process from the time you arrive until the time you leave takes about 30 minutes. The donation itself is only about 8-10 minutes on average. The steps in the process are </h1>
      <img class="imgr borderedbox inspace-5" src="images/banner.png" alt="">
      <h1>Before Donating Blood</h1>
      <p>Eat something substantial before your appointment.
Have plenty of liquid the day before donation, especially in warm weather. In the 3 hours before donating, have at least 3 to 4 good-sized glasses of water/juice</p>
      <h1>Registration of Donor</h1>
      <p>Our volunteer/staff will ask you to fill a form with brief information on contact details and medical history.
      </p>
      <h1>Physical Health check-up</h1>
      <p>You will be examined by a doctor to answer some questions about your health history. Your blood pressure and hemoglobin level will be checked.</p>
      <img class="imgl borderedbox inspace-5" src="images/sidebanner.jpg" alt="">
      <h1>Blood Donation</h1>
	  <p>We will cleanse an area on your arm and insert a brand new sterile needle for the blood draw. This feels like a quick pinch and is over in seconds.</p>
	  <p>The actual donation takes about 8-10 minutes, during which you will be seated comfortably.
When a unit of blood (350 – 450 ml) has been collected, the donation is complete.
A staff person will place a bandage on your arm
</p>
      <h1>Safety</h1>
	  <p>All equipment is sterile; needles are used only once and then discarded. In the great majority of individuals, a donation of 450 ml is less than 10% of your total body volume and may be given safely every 3 months. Your body keeps on discarding and replenishing blood all the time whether you give blood or not, so this amount is quickly replenished.
</p>
<h1>    AFTER DONATION
</h1>
<h1>Relax & Refresh</h1>
<p>
You can have something to eat and drink in and relax for about 15-20 minutes before departing. Within 24-48 hours your blood volume is completely restored. Apart from enjoying our refreshments and souvenir, other things to help you recover quickly include
</p>
<p>
Drink plenty of water, fruit juice etc. and avoid alcohol for 8 hours after you’ve given blood.
Avoid any strenuous activity or heavy lifting with the ‘donating arm’ for 24 hours after you’ve given blood.
If you become unwell in any way after giving blood, speak to the doctor in-charge straight away.
</p>

<h1>Donate Blood Again</h1>
<p>
Whole blood donors can give blood again after 3 months.
Apheresis plasma or platelet donors can give blood in around 2-3 weeks time.
</p>
<h1>Spread the word<h1>
<p>Tell your friends, colleagues and family about the wonderful feeling you experience on donating blood and saving lives. 
Enjoy the feeling of knowing that you helped save precious lives!
</p>
         
